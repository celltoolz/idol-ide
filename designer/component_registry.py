# ── Helpers ───────────────────────────────────────────────────────────────────

def all_component_types() -> list[str]:
    """Return all registered component type keys in display order."""
    return list(COMPONENT_REGISTRY.keys())


def get_component_def(type_key: str) -> ComponentDef | None:
    return COMPONENT_REGISTRY.get(type_key)


def default_props(type_key: str) -> dict[str, Any]:
    """Return a fresh props dict with all defaults for a component type."""
    cdef = COMPONENT_REGISTRY.get(type_key)
    if not cdef:
        return {}
    return {p.key: p.default for p in cdef.prop_defs}
