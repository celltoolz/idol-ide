from __future__ import annotations

"""
Non-visual component type registry for the IDOL Designer component tray.

Each entry in COMPONENT_REGISTRY defines how a component looks in the palette,
what properties it exposes in the Properties panel, what handler methods it
generates, and how its __init__ initialization is emitted in codegen.
"""

from dataclasses import dataclass, field
from typing import Any


@dataclass(frozen=True)
class PropDef:
    """Definition of one property row shown in the Properties panel for a component."""
    key:         str   # dict key in ComponentDescriptor.props
    label:       str   # display label in Properties tab
    kind:        str   # "int" | "bool" | "str" | "readonly"
    default:     Any   # value used when component is first created
    description: str   # hint bar text on hover


@dataclass(frozen=True)
class ComponentHandlerDef:
    """Definition of one handler method generated for a component."""
    id:            str   # short id used internally, e.g. "tick"
    label:         str   # method name suffix, e.g. "_tick"  (prepended with _{comp_id})
    description:   str   # hint bar text in the Handlers tab
    has_connector: bool  # True → show ⚡ connector button (can wire to a widget event)
    default_body:  str   # body used on first codegen; "" = use fixed template


@dataclass(frozen=True)
class ComponentDef:
    """Full definition of one component type."""
    type_key:        str                         # "Timer"
    label:           str                         # palette display name
    icon:            str                         # Unicode glyph shown in tray chip
    description:     str                         # palette tooltip / hint bar
    default_name:    str                         # auto-numbered base, e.g. "timer" → "timer1"
    prop_defs:       tuple[PropDef, ...]
    handler_defs:    tuple[ComponentHandlerDef, ...]
    codegen_imports: list[str] = field(default_factory=list)  # extra import lines ([] for Timer)


